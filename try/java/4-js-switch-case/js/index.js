let weather = "rainy"

switch (weather) {
    case "shunny":
        console.log("wear a sunglass")
        break;
    case "rainy":
        console.log("carry an umbrella")
         break
    case "windy":
        console.log("go fly kite")     
    default:
        console.log("stay at home and study")
        break;
}